package com.leetcode.challenge;

public class Stringsss {

	public static void main(String arg[]) {
		
		String firstname ="KarthikArul";
		String lastname = "Arul";
		
		for(int i=0;i<lastname.toCharArray().length;i++) {
			firstname="vijayakarthikeyan";
			lastname="";
		}
		
		System.out.println(firstname);
		
		
	}
}
